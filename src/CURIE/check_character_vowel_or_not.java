package CURIE;

public class check_character_vowel_or_not {

	
	public static void main(String[] args) {
		char ch = 'b';
		if(ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u' )
		System.out.println(ch + " is vowel");
		else
		System.out.println(ch + " is consonant");

	}

}
